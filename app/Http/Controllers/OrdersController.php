    public function order(Request $request)
    {
        $request->validate([
            'name' => 'required',
            'phone' => 'required',
        ]);

        $brand = new Orders;
        $brand->name = $request->name;
        $brand->phone = $request->phone;
        $brand->save();
        return redirect()->route('index')->with('success', 'Your Order Enquiry successfully Recieved.');
    }

